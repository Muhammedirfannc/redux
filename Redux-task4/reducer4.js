const initialState = {
    loading: false,
    users: [],
    error: ""
};

function reducer(state = initialState,action){
    switch(action.type){
        case "FETCH_START":
            return {...state,loading:true,error: ""};

        case "FETCH_SUCCESS":
            return {...state,loading:false,users:action.payload };

        case "FETCH_ERROR":
            return {...state,loading:false,error:action.payload};

        default:
            return state;

    }
}

export default reducer;